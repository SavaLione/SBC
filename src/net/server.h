#ifndef NET_SERVER_H
#define NET_SERVER_H

#include <cstdlib>
#include <iostream>
#include <memory>
#include <utility>

#include "net/session.h"

class server
{
public:
    server(boost::asio::io_context &io_context, short port)
        : acceptor_(io_context, boost::asio::ip::tcp::endpoint(boost::asio::ip::tcp::v4(), port))
    {
        do_accept();
    }

private:
    void do_accept();

    boost::asio::ip::tcp::acceptor acceptor_;
};

#endif // NET_SERVER_H